<script>
    $(document).ready(function () {

        function formatNumber(number) {
            return number.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ".") + " VNƒê";
        }

        // H√†m t√≠nh t·ªïng
        function calculateTotalSum() {
